class Car:
    def move(self):
        print(" Driving on the road!")

class Plane:
    def move(self):
        print(" Flying in the sky!")

class Boat:
    def move(self):
        print(" Sailing on the water!")

class Bicycle:
    def move(self):
        print(" Pedaling on the street!")

# Create a list of vehicles
vehicles = [Car(), Plane(), Boat(), Bicycle()]

# Use polymorphism: each .move() behaves differently
print("\n--- Vehicles in Action ---")
for v in vehicles:
    v.move()